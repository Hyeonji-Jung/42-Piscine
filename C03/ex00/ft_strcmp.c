int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int re;

	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
		{
			re = s1[i] - s2[i];
			return (re);
		}
		i++;
	}
	return (0);
}
