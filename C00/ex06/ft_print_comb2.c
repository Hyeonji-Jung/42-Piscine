#include <unistd.h>

void	ft_print_comb2(void)
{
	char a[5];

	a[0] = '0';
	a[1] = '0';
	a[2] = ' ';
	while (a[0] <= '9')
	{
		a[3] = a[0];
		a[4] = a[1] + 1;
		while (a[1] <= '8')
		{
			while (a[3] <= '9')
			{
				while (a[4] <= '9')
				{
					write(1, a, 5);
					if (a[0] == '9' && a[1] == '8') ;
					else
						write(1, ", ", 2);
					a[4]++;
				}
				a[4] = '0';
				a[3]++;
			}
			a[1]++;
			a[3] = a[0];
			a[4] = a[1] + 1;
		}
		a[0]++;
		a[1] = '0';
	}
}

int		main(void)
{
	ft_print_comb2();
	return (0);
}
