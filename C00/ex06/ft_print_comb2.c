#include <unistd.h>

int		write_comb(char *a)
{
	if (a[0] == '9' && a[1] == '8')
	{
		write(1, "98 99", 5);
		return (1);
	}
	write(1, a, 5);
	write(1, ", ", 2);
	a[4]++;
	return (0);
}

void	ft_print_comb2(void)
{
	char	a[5];

	a[0] = '0';
	a[1] = '0';
	a[2] = ' ';
	while (a[0] <= '9')
	{
		a[1] = '0';
		while (a[1] <= '9')
		{
			a[3] = a[0];
			a[4] = a[1] + 1;
			while (a[3] <= '9')
			{
				while (a[4] <= '9')
					if (write_comb(a))
						return ;
				a[3]++;
				a[4] = '0';
			}
			a[1]++;
		}
		a[0]++;
	}
}
