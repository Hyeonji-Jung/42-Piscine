int		ft_str_is_alpha(char *str)
{
	int		i;
	char	*a;

	i = 0;
	a = &str[i];
	if (str[0] == '\0')
		return (1);
	while (*a++)
	{
		if ((*a >= 'a' && *a <= 'z') || (*a >= 'A' && *a <= 'Z'))
			return (1);
	}
	return (0);
}
