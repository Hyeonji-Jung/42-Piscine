int		ft_str_is_alpha(char *str)
{
	int		i;
	char	c;

	i = 0;
	if (!str[0])
		return (1);
	while (str[i])
	{
		c = str[i];
		if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
			return (1);
		i++;
	}
	return (0);
}
